const KEY = "hb_seen_fact_v1";

export function getDailyFact(): HeightFact {
  const day = Math.floor(Date.now() / 86_400_000);
  return HEIGHT_FACTS[day % HEIGHT_FACTS.length];
}

export function markFactSeen(id: string) {
  try {
    localStorage.setItem(KEY, id);
  } catch {
    // ignore
  }
}

export function getLastSeenFactId(): string | null {
  try {
    return localStorage.getItem(KEY);
  } catch {
    return null;
  }
}
